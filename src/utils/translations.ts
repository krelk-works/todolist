export const getTranslation = () => {
    const browserLang = navigator.language;

    switch (browserLang) {
        case 'es-ES':
            return spanish;
        case 'en-US':
            return english;
        case 'ro-RO':
            return romanian;
        case 'fr-FR':
            return french;
        case 'it-IT':
            return italian;
        case 'de-DE':
            return german;
        case 'pt-PT':
            return portuguese;
        case 'bg-BG':
            return bulgarian;
        case 'tr-TR':
            return turkish;
        default:
            return english;
    }
};
